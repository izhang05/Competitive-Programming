void test_case() {
    long long n, a, b;
    cin >> n >> a >> b;
    if (b <= a) {
        cout << n * a << "\n";
        return;
    }
    long long d = b - a;
    long long k = min(d, n);
    long long sol = (b * (b + 1)) / 2 - ((b - k) * (b - k + 1)) / 2;
    cout << sol + (n - k) * a << "\n";
}

