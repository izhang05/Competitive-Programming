vector<int> adj[maxn];
pairnum<int, int> dp[maxn][2]; // <cnt, -sum_deg>
vector<pair<int, int>> child[maxn][2];
void dfs1(int c, int p) {
    dp[c][1] = {1, -(int) adj[c].size()};
    dp[c][0] = {0, -1};
    for (auto &i : adj[c]) {
        if (i != p) {
            dfs1(i, c);
            dp[c][1] += dp[i][0];
            child[c][1].emplace_back(i, 0);
            if (dp[i][0] > dp[i][1]) {
                dp[c][0] += dp[i][0];
                child[c][0].emplace_back(i, 0);
            } else {
                dp[c][0] += dp[i][1];
                child[c][0].emplace_back(i, 1);
            }
        }
    }
}

