void test_case() {
    int n, k;
    cin >> n >> k;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    sort(a.begin(), a.end());
    reverse(a.begin(), a.end());
    map<int, int> cnt;
    for (int i = 0; i < 2 * k; ++i) {
        ++cnt[a[i]];
    }
    long long sol = 0;
    for (auto &i : cnt) {
        sol += max(0, i.second - k);
    }
    for (int i = 2 * k; i < n; ++i) {
        sol += a[i];
    }
    cout << sol << "\n";
}

int main() {
    setIO("1");

    int test_case_number = 1;
    cin >> test_case_number;
    while (test_case_number--) {
        test_case();
    }
    return 0;
}
