void test_case() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    for (int i = *max_element(a.begin(), a.end());; ++i) {
        int b = 0, c = 0;
        for (auto &j : a) {
            b += j;
            c += i - j;
        }
        if (c > b) {
            cout << i << "\n";
            return;
        }
    }
}

