void setIO() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
#ifdef DEBUG
//freopen("solution.out", "w", stdout);
    freopen("1.in", "r", stdin);
#endif
}

void solve() {
    int n, k;
    cin >> n >> k;
    int sol = 0;
    int a, next = k;
    for (int i = 0; i < n; ++i) {
        cin >> a;
        if (a == next) {
            --next;
            if (!next) {
                ++sol;
                next = k;
            }
        } else {
            next = k;
            if (a == k) {
                --next;
            }
        }
    }
    cout << sol << "\n";
}

int main() {
    setIO();
    int t;
    cin >> t;
    for (int i = 1; i <= t; ++i) {
        cout << "Case #" << i << ": ";
        solve();
    }
}
